"""スリープ＆マインドLab - プロンプト定義

睡眠・メンタルヘルス特化ブログ用のプロンプトを一元管理する。
YMYL対策: 出典明記・断定表現回避・医師相談推奨
表現ルール: 「治る」「改善する」禁止 → 「期待される」「報告されている」に統一
"""


def build_keyword_prompt(config):
    """睡眠・メンタルヘルス特化キーワード選定プロンプトを構築する"""
    categories_text = "\n".join(f"- {cat}" for cat in config.TARGET_CATEGORIES)
    return (
        "睡眠とメンタルヘルスの専門ブログ用のキーワードを選定してください。\n\n"
        "以下のカテゴリから1つ選び、そのカテゴリで検索需要が高い"
        "睡眠・メンタルヘルス関連のキーワードを1つ提案してください。\n\n"
        f"カテゴリ一覧:\n{categories_text}\n\n"
        "【キーワード選定の基準】\n"
        "- ロングテールキーワード（3語以上の組み合わせ）を優先\n"
        "- 「睡眠 ○○ 改善」「不眠 ○○ 対策」「ストレス ○○ 解消法」などの具体的な組み合わせ\n"
        "- 「睡眠の質 上げる 方法」「寝つきが悪い 原因」「メンタル 回復 習慣」などの疑問・悩み系\n"
        "- 「GABA サプリ 睡眠」「マインドフルネス 初心者」「睡眠スコア 改善」などの具体系\n"
        "- 季節性のあるキーワードも考慮（冬季うつ、五月病、夏の寝苦しさなど）\n"
        "- 商品比較系: 「○○ vs ○○」「○○ おすすめ ランキング」\n\n"
        "以下の形式でJSON形式のみで回答してください（説明不要）:\n"
        '{"category": "カテゴリ名", "keyword": "キーワード"}'
    )


def build_article_prompt(keyword, category, config):
    """睡眠・メンタルヘルス記事生成プロンプトを構築する"""
    return f"""あなたは睡眠科学の研究者であり、
臨床経験豊富なメンタルヘルスカウンセラーでもあるサイエンスライターです。
最新の論文データと実践的なアドバイスを組み合わせて、読者の睡眠とメンタルヘルスの改善を支援する記事を書いてください。

以下のキーワードについて、科学的根拠に基づいた実用的な記事を書いてください。

【基本条件】
- ブログ名: {config.BLOG_NAME}
- キーワード: {keyword}
- カテゴリ: {category}
- 言語: 日本語
- 文字数: {config.MAX_ARTICLE_LENGTH}文字程度

【記事フォーマット】
## この記事の結論
- 結論1（最も重要なポイント）
- 結論2
- 結論3

## 科学的エビデンス
（関連する論文・研究データを引用して解説。
例：「○○大学の研究（2023年）では、○○が△△%改善したと報告されている」
具体的な数値やサンプルサイズを可能な限り含める。
出典は「（出典: ○○ et al., 20XX, ジャーナル名）」の形式で明記）

## 具体的な改善メソッド
（ステップバイステップで実践方法を解説。
各ステップに「なぜ効果が期待されるのか」の科学的根拠を添える。
所要時間・難易度・期待される変化の目安を明記）

## おすすめ商品・サプリ
（記事内容に関連する場合のみ記載。
成分名・作用機序・推奨摂取量・注意点を科学的に解説。
「※サプリメントは食品であり、医薬品ではありません」の注記を必ず記載）

## 注意点・やってはいけないこと
（よくある間違い、逆効果になる行動、医療機関を受診すべきサインを明記。
「以下の症状がある場合は自己判断せず、必ず医師に相談してください」を含める）

## まとめ：今日から始められるアクション
（すぐに実践できる具体的なアクションを3〜5つ箇条書き。
コスト0円でできることから順に紹介）

【YMYL対策（最重要・絶対厳守）】
- 「治る」「改善する」「効果がある」などの断定表現は絶対に使わない
- 代わりに以下の表現を使う:
  - 「〜の改善が期待される」
  - 「〜と報告されている」
  - 「〜の可能性が示唆されている」
  - 「〜に寄与する可能性がある」
  - 「〜という研究結果がある」
- 「※この記事は情報提供を目的としており、医療アドバイスではありません」を記事末に必ず記載
- 「症状が2週間以上続く場合は、医師や専門家にご相談ください」を必ず含める
- 精神疾患の診断・治療に関する断定は絶対にしない
- 薬の減薬・断薬を推奨する記述は絶対にしない
- 情報の根拠として論文名・研究機関・調査データなどの出典を明記

【SEO要件】
1. タイトルにキーワード「{keyword}」を必ず含めること
2. タイトルは32文字以内で魅力的に
3. H2、H3の見出し構造を適切に使用すること
4. キーワード密度は{config.MIN_KEYWORD_DENSITY}%〜{config.MAX_KEYWORD_DENSITY}%を目安に
5. メタディスクリプションは{config.META_DESCRIPTION_LENGTH}文字以内

【差別化ポイント】
- 論文・研究データの具体的な引用（著者名・年・ジャーナル名）
- 具体的な数値データ（「○○%改善」「△△分短縮」など）
- ステップバイステップの実践ガイド（時間・回数・頻度を明記）
- 睡眠ステージ（N1/N2/N3/REM）やホルモン（メラトニン・コルチゾール等）の科学解説
- 内部リンクのプレースホルダーを2〜3箇所に配置（{{{{internal_link:関連トピック}}}}の形式）

【出力形式】
以下のJSON形式で出力してください。JSONブロック以外のテキストは出力しないでください。

```json
{{
  "title": "SEO最適化されたタイトル",
  "content": "# タイトル\\n\\n本文（Markdown形式）...",
  "meta_description": "120文字以内のメタディスクリプション",
  "tags": ["タグ1", "タグ2", "タグ3", "タグ4", "タグ5"],
  "slug": "url-friendly-slug"
}}
```

【注意事項】
- content内のMarkdownは適切にエスケープしてJSON文字列として有効にすること
- tagsは5個ちょうど生成すること
- slugは半角英数字とハイフンのみ使用すること
- 専門用語には必ず（カッコ）で平易な補足説明を付けること
- 他の睡眠ブログにはない科学的深掘り情報を必ず1つ以上含めること
- 読者が不安を感じる表現は避け、前向きで実践的なトーンを維持すること"""
